    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        String insere = JOptionPane.showInputDialog("Digite o valor: ");
        int num;

        try {
            num = Integer.parseInt(insere);

            if (num >= 0) {
                this.arvore.insere(num);
                this.arvore.showArvore();
                arvore2.inserir(num);
                buscas.add(num);
                
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(panel, "Valor inválido. Digite um número", "Valor Inválido", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed
    
    
    
