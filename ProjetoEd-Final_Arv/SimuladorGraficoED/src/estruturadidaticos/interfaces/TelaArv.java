    public boolean pesquisa(){
        int elemento = Integer.parseInt(busca);
        
        for(int j = 0; j < buscas.size(); j++) {
                
            if(buscas.get(j) == elemento){
                
                pegaNum = buscas.get(j);
                return true;
            }
        } 
        return false;
    }
     
    private boolean isNumber(String campo){           
            return campo.matches("[0-9]+");   
    }
